#CORPORA :Following is a list of text corpora in various languages. "Text corpora" is the plural of "text corpus".
# A text corpus is a large and unstructured set of texts (nowadays usually electronically stored and processed).
# Text corpora are used to do statistical analysis and hypothesis testing, checking occurrences or validating linguistic
# rules within a specific language territory.

#WORDNET : WordNet is a large lexical database of English. Nouns, verbs, adjectives and adverbs are grouped into sets
# of cognitive synonyms (synsets), each expressing a distinct concept. Synsets are interlinked by means of
# conceptual-semantic and lexical relations